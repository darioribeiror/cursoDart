void main(){
/*
  Comparadores:
    > Maior
    >= Maior Igual
    < Menor
    <= Menor igual
    == Igual
    != Diferente
*/

  int num1 = 2;
  int num2 = 1;

  if(num1 > num2){
    print("$num1 é maior que o $num2");
  }  else if(num1 < num2){
    print("$num1 é menor que o $num2");
  }  else {
    print("$num1 é igual ao $num2");
  }  
}